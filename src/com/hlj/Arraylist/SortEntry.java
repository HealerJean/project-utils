public class SortEntry {
	 private int age;  
	    private int score;  
	      
	    public SortEntry(int age, int score){  
	        this.age = age;  
	        this.score = score;  
	    }  
	      
	    public int getAge() {  
	        return age;  
	    }  
	    public void setAge(int age) {  
	        this.age = age;  
	    }  
	    public int getScore() {  
	        return score;  
	    }  
	    public void setScore(int score) {  
	        this.score = score;  
	    }  
}
